		    <h2 class="currentpapertitle" id="section1">EvoLang XI Electronic Proceedings</h2>
            <p>Welcome to the electronic proceedings for the 11th International Conference on the Evolution of Language (EvoLang XI).  For the first time, the EvoLang proceedings are available, open access, in digital format.
            <p>EvoLang XI is organised by <a href="https://www.usm.edu/gulfcoast/evolang-2016">The University of Southern Mississippi</a> and takes place in New Orleans, at the <a href="http://tulane.edu/studentaffairs/lbc/">Lavin-Bernick Center for University Life</a> at Tulane.  For more information, please visit the <a href="https://www.usm.edu/gulfcoast/evolang-2016/home">EvoLang XI Homepage</a>.  For more information about the EvoLang conferences, visit the <a href="http://www.evolang.org/">EvoLang website</a>.
            <p> <a href="toc/toc.html">Access the proceedings</a>
              <p> Please note that the submissions may be subject to updates until the date of the conference (21st March, 2016).
            <h3>Talk Schedule</h3>
            <p> The talk schedule is available <a href="schedule/Evolang 2016 Full Schedule.pdf">here</a> (version 2).
            <p>The Plenary talk abstracts are <a href="plenaries.html">available here</a>.
            <h3>Scientific committee</h3>
            <p>The proceedings were edited by <a href="http://www.mpi.nl/people/roberts-sean">Seán Roberts</a>, <a href="https://ccuskley.wordpress.com/">Christine Cuskley</a>, <a href="http://www.evolutionarylinguistics.org/luke-mccrohon.html">Luke McCrohon</a>, <a href="https://www.researchgate.net/profile/Barcelo-Coblijn_Lluis">Lluís Barceló-Coblijn</a>, <a href="https://sites.google.com/site/olcifeher/">Olga Feher</a> and <a href="http://crl.ucsd.edu/~tverhoef/">Tessa Verhoef</a>.

            <h3>Citation</h3>
            <p>The proceedings may be cited as:
            <p>Roberts S.G., Cuskley C., McCrohon L., Barceló-Coblijn L. , Feher O. & Verhoef T. (Eds.) (2016) The Evolution of Language: Proceedings of the 11th International Conference (EVOLANG11).<a href="http://dx.doi.org/10.17617/2.2248195">doi:10.17617/2.2248195</a>.

            <h3>Sharing</h3>
            <p> Most papers in the proceedings are shared under a creative commons license, allowing others to distribute them with attribution and as long as they are not modified.  However, please check the license for each paper (at the bottom of the paper's webpage) before distributing.

